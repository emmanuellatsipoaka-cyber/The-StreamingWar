# 10. ESQUISSE NCF — Neural Collaborative Filtering  [C7]
# =============================================================================

class NCFSketch:
    """
    [C7] Esquisse de Neural Collaborative Filtering (He et al., 2017).

    Architecture :
        Input : (user_idx, movie_idx)
        ↓
        Embedding User (k)  ‖  Embedding Movie (k)   ← MF branch
        Embedding User (h)  ‖  Embedding Movie (h)   ← MLP branch
        ↓
        MF : produit élément-par-élément → (k,)
        MLP : concat → Dense(2h) → ReLU → Dense(h) → ReLU → Dense(h/2) → ReLU
        ↓
        NeuMF : concat MF + MLP → Dense(1) → sigmoid × 5

    Implémenté en numpy pur (pour la démonstration — en production : PyTorch).

    Complexité paramètres (k=20, h=64) :
        Embeddings users  : 6040 × (20+64) ≈ 507K params
        Embeddings movies : 3883 × (20+64) ≈ 326K params
        MLP layers        : ~25K params
        Total             : ~860K params  (très léger)
    """

    def __init__(self, n_users, n_movies, k_mf=20, k_mlp=64):
        self.k_mf   = k_mf
        self.k_mlp  = k_mlp
        rng = np.random.default_rng(42)

        # Embeddings
        self.E_user_mf  = rng.normal(0, 0.01, (n_users,  k_mf))
        self.E_movie_mf = rng.normal(0, 0.01, (n_movies, k_mf))
        self.E_user_mlp = rng.normal(0, 0.01, (n_users,  k_mlp))
        self.E_movie_mlp= rng.normal(0, 0.01, (n_movies, k_mlp))

        # MLP layers (non entraîné ici — squelette)
        h = k_mlp
        self.W1 = rng.normal(0, 0.01, (2 * h, 2 * h))
        self.W2 = rng.normal(0, 0.01, (2 * h, h))
        self.W3 = rng.normal(0, 0.01, (h, h // 2))
        self.W_out = rng.normal(0, 0.01, (k_mf + h // 2, 1))

    def _relu(self, x):
        return np.maximum(0, x)

    def predict(self, user_idx, movie_idx):
        """Forward pass pour un exemple (démonstration)."""
        # MF branch
        mf = self.E_user_mf[user_idx] * self.E_movie_mf[movie_idx]  # (k_mf,)

        # MLP branch
        x = np.concatenate([
            self.E_user_mlp[user_idx],
            self.E_movie_mlp[movie_idx]
        ])  # (2*k_mlp,)
        x = self._relu(x @ self.W1)
        x = self._relu(x @ self.W2)
        x = self._relu(x @ self.W3)   # (k_mlp/2,)

        # Fusion NeuMF
        concat = np.concatenate([mf, x])  # (k_mf + k_mlp/2,)
        score  = float(concat @ self.W_out) * 5.0  # échelle [0,5]
        return np.clip(score, 1.0, 5.0)

    def summary(self):
        params = (
            self.E_user_mf.size + self.E_movie_mf.size +
            self.E_user_mlp.size + self.E_movie_mlp.size +
            self.W1.size + self.W2.size + self.W3.size + self.W_out.size
        )
        print("\n  ┌─ Architecture NCF (He et al., 2017) ─────────────────┐")
        print(f"  │  Embeddings MF   : users {self.E_user_mf.shape}  "
              f"movies {self.E_movie_mf.shape}   │")
        print(f"  │  Embeddings MLP  : users {self.E_user_mlp.shape}  "
              f"movies {self.E_movie_mlp.shape} │")
        print(f"  │  MLP layers      : {self.W1.shape} → {self.W2.shape}"
              f" → {self.W3.shape}       │")
        print(f"  │  Total paramètres: {params:,}                        │")
        print(f"  │  Amélioration vs ALS : capture relations non-linéaires│")
        print(f"  │  À entraîner avec : PyTorch + BCELoss + Adam          │")
        print("  └──────────────────────────────────────────────────────┘")


