class ColdStartHandler:
    """
    Trois stratégies. La stratégie C (démo) est vectorisée [C4].
    """

    def __init__(self, ratings, movies, movie_map, demo=None):
        self.ratings   = ratings
        self.movies    = movies
        self.movie_map = movie_map
        self.demo      = demo
        self._title    = movies.set_index("movie_id")["title"].to_dict()
        self._compute_popularity()

        # [C4] Table pivot ratings pour vectoriser l'agrégation démo
        # {movie_id: {user_id: rating}}  → pivot sparse
        self._build_rating_pivot()

    def _compute_popularity(self):
        stats = self.ratings.groupby("movie_id").agg(
            count=("rating", "count"),
            mean=("rating", "mean")
        ).reset_index()
        m = 50
        gm = self.ratings["rating"].mean()
        stats["score"] = (
            (stats["count"] / (stats["count"] + m)) * stats["mean"]
            + (m / (stats["count"] + m)) * gm
        )
        self.popularity    = stats.sort_values("score", ascending=False)
        self._pop_score    = stats.set_index("movie_id")["score"].to_dict()

    def _build_rating_pivot(self):
        """[C4] Pivot vectorisé : user_id → array de (movie_id, rating)."""
        # On construit un DataFrame pivot compact (sparse)
        self._neighbor_ratings_df = (
            self.ratings[["user_id", "movie_id", "rating"]]
            .copy()
        )

    def recommend_by_popularity(self, K=10, exclude_ids=None):
        pop = self.popularity
        if exclude_ids:
            pop = pop[~pop["movie_id"].isin(exclude_ids)]
        top = pop.head(K).merge(self.movies[["movie_id", "title"]], on="movie_id")
        return top[["movie_id", "title", "score"]].values.tolist()

    def recommend_by_genre(self, preferred_genres, K=10):
        mask = self.movies["genres"].apply(
            lambda g: any(genre in g for genre in preferred_genres)
        )
        result = (self.movies[mask]
                  .merge(self.popularity, on="movie_id")
                  .sort_values("score", ascending=False)
                  .head(K))
        return result[["movie_id", "title", "score"]].values.tolist()

    def recommend_by_demo(self, user_id, already_rated_ids=None, K=10):
        """
        [C4] Agrégation vectorisée des ratings des voisins démo.
        Pas d'iterrows : tout passe par des opérations pandas/numpy.
        """
        if self.demo is None:
            return self.recommend_by_popularity(K=K, exclude_ids=already_rated_ids)

        neighbor_ids, neighbor_sims = self.demo.get_similar_users(user_id, K=50)
        if len(neighbor_ids) == 0:
            return self.recommend_by_popularity(K=K, exclude_ids=already_rated_ids)

        sim_series = pd.Series(neighbor_sims, index=neighbor_ids)

        # [C4] Filtrage vectorisé des ratings des voisins
        mask = self._neighbor_ratings_df["user_id"].isin(neighbor_ids)
        df   = self._neighbor_ratings_df[mask].copy()

        if already_rated_ids:
            df = df[~df["movie_id"].isin(already_rated_ids)]

        if df.empty:
            return self.recommend_by_popularity(K=K, exclude_ids=already_rated_ids)

        # Pondération par similarité
        df["sim"]            = df["user_id"].map(sim_series)
        df["weighted_rating"] = df["rating"] * df["sim"]

        # Agrégation vectorisée
        agg = (df.groupby("movie_id")
                 .agg(num=("weighted_rating", "sum"), den=("sim", "sum"))
                 .reset_index())

        # Handle ZeroDivisionError if 'den' is 0
        agg["score"] = np.where(agg["den"] == 0, 0, agg["num"] / agg["den"])
        agg = agg.sort_values("score", ascending=False).head(K)

        return [
            (row["movie_id"], self._title.get(row["movie_id"], "Unknown"),
             round(row["score"], 3))
            for _, row in agg.iterrows()
        ]


