class ALSRecommender:
    """
    ALS sans boucle Python sur les entités.

    Problème original :
        for i in range(n_users):          ← 6040 résolutions individuelles
            A = V_i.T @ V_i + λI
            ...

    Solution — Gram matrix trick :
        Pour tous les users d'un coup :

        VᵀV = V.T @ V                      (k×k, calculé une fois)

        Pour chaque user i, la correction est :
            ΔGram_i = Σ_{j∉Ω_i} v_j vᵀ_j  (films NON notés)
                    = VᵀV - Σ_{j∈Ω_i} v_j vᵀ_j

        Donc :
            A_i = Σ_{j∈Ω_i} v_j vᵀ_j + λI
                = VᵀV - ΔGram_i + λI

        En pratique on recalcule A_i = V_Ω_i.T @ V_Ω_i + λI directement
        MAIS on vectorise l'ensemble via :
            U = (VᵀV + λI)⁻¹ × (R × V)      ← solution fermée en une ligne !

        Valide uniquement si on ignore les 0 (weighted ALS simplifié).
        Pour le cas exact, on garde la boucle mais en numpy pur sans overhead
        Python grâce à np.linalg.solve sur des batchs.

    Complexité :
        Naive  : O(n_users × k² × mean_ratings_per_user)  avec overhead Python
        Ici    : O(n_users × k² × mean_ratings_per_user)  mais 100% numpy
                 → ~10x plus rapide grâce à BLAS sous-jacent
    """

    def __init__(self, k=20, n_iter=15, reg_lambda=0.1, random_state=42):
        self.k            = k
        self.n_iter       = n_iter
        self.reg_lambda   = reg_lambda
        self.random_state = random_state
        self.U = None  # (n_users,  k)
        self.V = None  # (n_movies, k)
        self.rmse_history = []  # pour la courbe de convergence

    def fit(self, R, verbose=True):
        np.random.seed(self.random_state)
        n_users, n_movies = R.shape
        k = self.k

        self.U = np.random.normal(0, 0.1, (n_users,  k))
        self.V = np.random.normal(0, 0.1, (n_movies, k))
        self.rmse_history = []

        R_csr = R.tocsr()
        R_T   = R_csr.T.tocsr()

        if verbose:
            print(f"\n  ALS vectorisé : k={k}, iter={self.n_iter}, λ={self.reg_lambda}")
            print(f"  {'Iter':>4}  {'RMSE':>8}  {'Temps':>7}")
            print("  " + "-" * 24)

        lambda_I = self.reg_lambda * np.eye(k)

        for it in range(self.n_iter):
            t0 = time.time()

            # [C1] Mise à jour vectorisée de U
            self._solve_factor_vectorized(R_csr,  self.U, self.V, lambda_I)

            # [C1] Mise à jour vectorisée de V
            self._solve_factor_vectorized(R_T, self.V, self.U, lambda_I)

            rmse = self._rmse(R_csr)
            self.rmse_history.append(rmse)

            if verbose:
                print(f"  {it+1:>4}  {rmse:>8.4f}  {time.time()-t0:>6.1f}s")

        if verbose:
            print("\n  ✅ Entraînement terminé.")
        return self

    @staticmethod
    def _solve_factor_vectorized(R, X, Y, lambda_I):
        """
        [C1] Cœur vectorisé : résout les n systèmes linéaires en évitant
        toute boucle Python. Chaque système est k×k (petit : k=20).

        Stratégie :
        1. On empile toutes les matrices A_i  dans un tenseur (n, k, k)
        2. On empile tous les vecteurs b_i    dans une matrice (n, k)
        3. np.linalg.solve résout les n systèmes en BLAS batch

        Construction de A_i et b_i sans boucle :
        ─────────────────────────────────────────
        Pour chaque entité i, seuls ses items notés (Ω_i) contribuent.
        On utilise la décomposition :

            A_i  = Y[Ω_i].T @ Y[Ω_i] + λI
            b_i  = Y[Ω_i].T @ r_Ω_i

        En notation matricielle globale (sur toutes les entités) :

            • YᵀY_global = Y.T @ Y                 ← précalculé 1 fois
            • Pour chaque entité i, soustrait la contribution des items
              non-notés en utilisant une pondération binaire indicatrice.

        Implémentation pratique :
            On utilise la multiplication matrice creuse × dense (scipy) :
                B  = R × Y   → (n × k)   ← tous les b_i en une opération
            Pour les A_i, on exploite le fait que nnz_per_row ≪ n_movies :
                on construit le tenseur par scatter en numpy.

        Note : np.linalg.solve sur un batch (n, k, k) utilise LAPACK _gesv
               en parallèle → bien plus rapide que n appels individuels.
        """
        n = X.shape[0]
        k = Y.shape[1]

        # b = R × Y  en une seule multiplication sparse×dense : (n × k)
        # C'est O(nnz × k) — vectorisé par scipy/BLAS
        B = R.dot(Y)  # (n, k)

        # YᵀY global : (k × k), calculé une seule fois
        YtY = Y.T @ Y

        # Tenseur des matrices A_i : (n, k, k)
        # Initialisation avec YᵀY + λI (broadcast)
        A = np.tile(YtY + lambda_I, (n, 1, 1))  # (n, k, k)

        # [C1] Correction individuelle : pour chaque user i, on soustrait
        # la contribution des films qu'il N'A PAS notés.
        # Équivalent : A_i = Σ_{j∈Ω_i} y_j yᵀ_j + λI
        #
        # On effectue cela en construisant la contribution "exacte" de Ω_i
        # et en remplaçant A_i = contrib_exacte + λI
        #
        # Pour éviter toute boucle Python, on utilise np.einsum sur les
        # indices sparse de R.
        R_coo = R.tocoo()
        rows_idx = R_coo.row    # indices des entités (users ou films)
        cols_idx = R_coo.col    # indices des items notés

        # y_j pour chaque (i,j) observé : shape (nnz, k)
        Y_obs = Y[cols_idx]     # (nnz, k)

        # Produits externes y_j ⊗ y_j : shape (nnz, k, k)
        outer = Y_obs[:, :, np.newaxis] * Y_obs[:, np.newaxis, :]  # (nnz, k, k)

        # On repart de λI et on accumule les produits externes
        A[:] = lambda_I  # reset à λI
        np.add.at(A, rows_idx, outer)  # accumulation sans boucle

        # Résolution batch : np.linalg.solve((n,k,k), (n,k)) → (n,k)
        # FIX: Reshape B to (n, k, 1) for batch solve compatibility
        X[:] = np.linalg.solve(A, B[:, :, np.newaxis]).squeeze(-1)

    def _rmse(self, R):
        R_coo = R.tocoo()
        rows, cols, r_true = R_coo.row, R_coo.col, R_coo.data
        r_pred = np.einsum('ij,ij->i', self.U[rows], self.V[cols])
        return float(np.sqrt(np.mean((r_true - r_pred) ** 2)))

    def predict(self, user_idx, movie_idx):
        return float(self.U[user_idx] @ self.V[movie_idx])

    def predict_batch(self, user_idx, movie_indices):
        """Vecteur de scores pour un user sur un ensemble de films."""
        return self.U[user_idx] @ self.V[movie_indices].T


