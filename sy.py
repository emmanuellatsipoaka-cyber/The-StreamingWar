class DemographicProfiler:
    """
    Encode les features démographiques et calcule les similarités.

    [C4] La recherche de voisins et l'agrégation des scores sont
    entièrement vectorisées (pas d'iterrows).
    """

    AGE_MAP = {1: 0, 18: 1, 25: 2, 35: 3, 45: 4, 50: 5, 56: 6}

    def __init__(self):
        self.user_vectors   = None   # (n_users, n_features) normalisé
        self.user_ids_array = None   # tableau numpy ordonné
        self._uid_to_pos    = {}

    def fit(self, users):
        df = users.copy()
        df["g_enc"]   = (df["gender"] == "M").astype(float)
        df["age_enc"] = df["age"].map(self.AGE_MAP).fillna(3).astype(float)
        df["occ_enc"] = df["occupation"].astype(float)

        X = df[["g_enc", "age_enc", "occ_enc"]].values
        scaler = MinMaxScaler()
        X_norm = scaler.fit_transform(X)

        self.user_vectors   = X_norm.astype(np.float32)
        self.user_ids_array = df["user_id"].values
        self._uid_to_pos    = {uid: i for i, uid in enumerate(self.user_ids_array)}
        print(f"  Profil démo : {X_norm.shape[0]} users × {X_norm.shape[1]} features")
        return self

    def get_similar_users(self, user_id, K=20):
        """Retourne (user_ids, similarities) — entièrement vectorisé."""
        pos = self._uid_to_pos.get(user_id)
        if pos is None:
            return np.array([]), np.array([])
        vec  = self.user_vectors[pos].reshape(1, -1)
        sims = cosine_similarity(vec, self.user_vectors)[0]
        sims[pos] = -1
        top_idx = np.argpartition(sims, -K)[-K:]
        top_idx = top_idx[np.argsort(sims[top_idx])[::-1]]
        return self.user_ids_array[top_idx], sims[top_idx]

    def get_vector(self, user_id):
        pos = self._uid_to_pos.get(user_id)
        return self.user_vectors[pos] if pos is not None else None


