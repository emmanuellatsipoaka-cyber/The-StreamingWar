def calibrate_weights(rec_system, ratings_val, candidates=(0.1, 0.2, 0.3, 0.4)):
    """
    [C2] Calibration du poids démographique par Grid Search léger.

    Principe : sur un ensemble de validation, on cherche α* tel que :
        score_hybride = (1-α) × score_ALS + α × score_démo
    minimise le RMSE de ranking (Précision@10).

    Retourne le meilleur α et le tableau des résultats.
    """
    print("\n  Calibration pondération (Grid Search)...")
    results = []
    sample_users = ratings_val["user_id"].unique()[:100]

    for alpha in candidates:
        hits_total = 0
        count      = 0
        for uid in sample_users:
            if uid not in rec_system.user_map:
                continue
            user_idx = rec_system.user_map[uid]
            relevant = set(
                ratings_val[(ratings_val["user_id"] == uid) &
                             (ratings_val["rating"] >= 4)]["movie_id"]
            )
            if not relevant:
                continue

            already = rec_system.user_rated.get(uid, set())
            already_idx = {rec_system.movie_map[m] for m in already
                           if m in rec_system.movie_map}

            top_idx = rec_system.index.get_top_k(user_idx,
                                                  already_rated=already_idx, K=30)
            if not top_idx:
                continue

            als_scores  = rec_system.als_model.predict_batch(
                user_idx, np.array(top_idx))
            als_norm    = _minmax_norm(als_scores)
            demo_scores = rec_system._demo_scores(uid, top_idx)

            hybrid = (1 - alpha) * als_norm + alpha * demo_scores
            order = np.argsort(hybrid)[::-1][:10]
            recs = {rec_system.idx_to_movie[top_idx[i]] for i in order}
            hits_total += len(recs & relevant)
            count      += 1

        precision = hits_total / (10 * count) if count > 0 else 0
        results.append({"alpha": alpha, "precision@10": round(precision, 4)})
        print(f"    α={alpha:.1f}  →  Précision@10={precision:.4f}")

    best = max(results, key=lambda x: x["precision@10"])
    print(f"  → Meilleur α = {best['alpha']}  "
          f"(Précision@10={best['precision@10']})")
    return best["alpha"], pd.DataFrame(results)


def _minmax_norm(arr):
    lo, hi = arr.min(), arr.max()
    return (arr - lo) / (hi - lo) if hi != lo else np.ones_like(arr)


