def evaluate_full(rec_system, ratings_test, Ks=(5, 10, 20),
                  n_sample=500, verbose=True):
    """
    [C5] Évaluation complète :
    - RMSE sur ratings individuels
    - Précision@K et Rappel@K pour plusieurs valeurs de K
    - Comparaison avec 2 baselines :
        • Baseline naïve   : prédire la note moyenne globale
        • Baseline popularité : recommander les N films les plus populaires
    - Retourne un DataFrame tableau récapitulatif
    """

    users_test  = ratings_test["user_id"].unique()
    sample      = users_test[:n_sample]
    global_mean = rec_system.ratings["rating"].mean()
    popular_ids = set(
        rec_system.ratings.groupby("movie_id")["rating"]
        .count().nlargest(max(Ks)).index
    )

    rmse_als, rmse_baseline = [], []
    metrics = {k: {"hits": 0, "prec_base": 0, "rec_base": 0, "n": 0}
               for k in Ks}

    for uid in sample:
        if uid not in rec_system.user_map:
            continue
        uidx = rec_system.user_map[uid]

        user_test = ratings_test[ratings_test["user_id"] == uid]
        if user_test.empty:
            continue

        # RMSE
        for row in user_test.itertuples(index=False):
            mid = row.movie_id
            if mid not in rec_system.movie_map:
                continue
            midx  = rec_system.movie_map[mid]
            pred  = rec_system.als_model.predict(uidx, midx)
            rmse_als.append((row.rating - pred) ** 2)
            rmse_baseline.append((row.rating - global_mean) ** 2)

        relevant = set(user_test[user_test["rating"] >= 4]["movie_id"])
        if not relevant:
            continue

        for K in Ks:
            recs   = rec_system.recommend(uid, K=K)
            rec_ids = {r["movie_id"] for r in recs}
            hits   = len(rec_ids & relevant)

            base_hits = len(popular_ids & relevant)

            metrics[K]["hits"]      += hits
            metrics[K]["prec_base"] += base_hits / K
            metrics[K]["rec_base"]  += base_hits / len(relevant)
            metrics[K]["n"]         += 1

    rmse_val  = float(np.sqrt(np.mean(rmse_als)))      if rmse_als      else float('nan')
    rmse_base = float(np.sqrt(np.mean(rmse_baseline))) if rmse_baseline else float('nan')

    rows = []
    for K in Ks:
        n = metrics[K]["n"]
        if n == 0:
            continue
        prec = metrics[K]["hits"] / (K * n)
        rec  = metrics[K]["hits"] / max(1, sum(
            len(ratings_test[(ratings_test["user_id"] == uid) &
                             (ratings_test["rating"] >= 4)])
            for uid in sample[:n] if uid in rec_system.user_map
        ))
        rows.append({
            "Modèle"          : "ALS Hybride",
            "K"               : K,
            f"Précision@{K}"  : round(prec, 4),
            f"Rappel@{K}"     : round(rec, 4),
            "RMSE"            : round(rmse_val, 4),
            "RMSE_baseline"   : round(rmse_base, 4),
            f"Prec@{K}_base"  : round(metrics[K]["prec_base"] / n, 4),
        })

    df = pd.DataFrame(rows)

    if verbose:
        print("\n" + "=" * 65)
        print("  TABLEAU D'ÉVALUATION")
        print("=" * 65)
        print(f"  RMSE ALS Hybride  : {rmse_val:.4f}")
        print(f"  RMSE Baseline     : {rmse_base:.4f}  (prédire la moyenne)")
        print()
        print(df.to_string(index=False))
        print("=" * 65)

    return df


def rmse_vs_k_curve(R_train, R_val, k_values=(5, 10, 20, 40, 60),
                    n_iter=10, reg_lambda=0.1):
    """
    [C5] Courbe RMSE en fonction du nombre de facteurs latents k.
    Permet de détecter le surapprentissage.
    """
    print("\n  Courbe RMSE(k) — détection surapprentissage")
    print(f"  {'k':>5}  {'RMSE train':>12}  {'RMSE val':>10}")
    print("  " + "-" * 32)

    results = []
    for k in k_values:
        model = ALSRecommender(k=k, n_iter=n_iter, reg_lambda=reg_lambda)
        model.fit(R_train, verbose=False)
        r_train = model._rmse(R_train)
        r_val    = model._rmse(R_val)
        print(f"  {k:>5}  {r_train:>12.4f}  {r_val:>10.4f}")
        results.append({"k": k, "RMSE_train": r_train, "RMSE_val": r_val})

    return pd.DataFrame(results)


