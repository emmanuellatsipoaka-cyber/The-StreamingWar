class StreamingRecommender:
    """
    Interface hybride ALS + démographie avec :
    - [C1] ALS vectorisé
    - [C2] pondération calibrée
    - [C3] rate_movie() O(1) sans itération DF
    - [C4] cold start vectorisé
    """

    def __init__(self, k=20, n_iter=15, reg_lambda=0.1, top_k=20, alpha=0.30):
        self.als_model  = ALSRecommender(k=k, n_iter=n_iter, reg_lambda=reg_lambda)
        self.top_k_K    = top_k
        self.alpha      = alpha   # poids démographie (calibré)
        self.index      = None
        self.cold_start = None
        self.demo       = None
        self.user_map   = None
        self.movie_map  = None
        self.idx_to_movie = {}
        self._title     = {}
        self.ratings    = None

        # [C3] Table en mémoire : user_idx → {movie_idx: rating}
        self._ratings_by_user_idx = {}

    def fit(self, ratings, movies, users):
        self.ratings = ratings.copy()
        self.movies  = movies
        self._title  = movies.set_index("movie_id")["title"].to_dict()

        R, self.user_map, self.movie_map = build_sparse_matrix(ratings)
        self.idx_to_movie = {v: k for k, v in self.movie_map.items()}

        # [C3] Pré-indexation des ratings en mémoire (O(1) par user)
        self._build_memory_index(ratings)

        self.demo = DemographicProfiler()
        self.demo.fit(users)

        self.als_model.fit(R)

        self.index = TopKIndex(self.als_model.U, self.als_model.V, K=self.top_k_K)

        self.user_rated = ratings.groupby("user_id")["movie_id"].apply(set).to_dict()

        self.cold_start = ColdStartHandler(ratings, movies, self.movie_map, self.demo)

    def _build_memory_index(self, ratings):
        """[C3] Index mémoire : {user_idx: {movie_idx: rating}}"""
        for row in ratings.itertuples(index=False):
            uidx = self.user_map.get(row.user_id)
            midx = self.movie_map.get(row.movie_id)
            if uidx is None or midx is None:
                continue
            if uidx not in self._ratings_by_user_idx:
                self._ratings_by_user_idx[uidx] = {}
            self._ratings_by_user_idx[uidx][midx] = float(row.rating)

    def _demo_scores(self, user_id, movie_idxs):
        """[C2] Scores démographiques vectorisés pour un ensemble de films."""
        neighbor_ids, neighbor_sims = self.demo.get_similar_users(user_id, K=20)
        if len(neighbor_ids) == 0:
            return np.zeros(len(movie_idxs), dtype=np.float32)

        sim_by_uid = dict(zip(neighbor_ids.tolist(), neighbor_sims.tolist()))
        movie_ids  = [self.idx_to_movie[i] for i in movie_idxs]

        neighbor_df = self.ratings[self.ratings["user_id"].isin(neighbor_ids)]
        neighbor_df = neighbor_df[neighbor_df["movie_id"].isin(movie_ids)]

        if neighbor_df.empty:
            return np.zeros(len(movie_idxs), dtype=np.float32)

        neighbor_df = neighbor_df.copy()
        neighbor_df["sim"] = neighbor_df["user_id"].map(sim_by_uid)

        # Ensure 'weighted_rating' column is created before aggregation
        neighbor_df["weighted_rating"] = neighbor_df["rating"] * neighbor_df["sim"]

        # Agrégation vectorisée
        agg = (neighbor_df.groupby("movie_id")
                 .agg(num=("weighted_rating", "sum"), den=("sim", "sum"))
                 .reset_index())

        agg["score"] = np.where(agg["den"] == 0, 0, agg["num"] / agg["den"])
        # Ensure all movie_ids from input are present in agg, fill missing with 0 score
        agg = agg.set_index("movie_id").reindex(movie_ids, fill_value=0.0).reset_index()
        agg = agg.sort_values("score", ascending=False)

        return (agg["score"].values / 5.0).astype(np.float32)  # normalisé [0,1]

    def recommend(self, user_id, K=10):
        """Recommandation hybride O(1) lookup + scoring vectorisé."""
        if user_id not in self.user_map:
            return self.cold_start.recommend_by_demo(user_id, K=K)

        user_idx   = self.user_map[user_id]
        rated_ids  = self.user_rated.get(user_id, set())
        rated_idx  = {self.movie_map[m] for m in rated_ids if m in self.movie_map}

        pool = self.index.get_top_k(user_idx, already_rated=rated_idx,
                                    K=max(K * 3, 30))
        if not pool:
            return []

        pool_arr    = np.array(pool)
        als_scores  = self.als_model.predict_batch(user_idx, pool_arr)
        als_norm    = _minmax_norm(als_scores)

        # [C2] Pondération calibrée
        demo_sc     = self._demo_scores(user_id, pool)
        hybrid      = (1 - self.alpha) * als_norm + self.alpha * demo_sc

        order = np.argsort(hybrid)[::-1][:K]
        return [
            {
                "movie_id"   : self.idx_to_movie[pool_arr[i]],
                "title"      : self._title.get(self.idx_to_movie[pool_arr[i]], "?"),
                "score_als"  : round(float(als_scores[i]), 3),
                "score_hybrid": round(float(hybrid[i]), 3)
            }
            for i in order
        ]

    def rate_movie(self, user_id, movie_id, rating):
        """
        [C3] Enregistre une notation et met à jour u_i sans itération DF.

        Stratégie :
        1. Mise à jour de l'index mémoire O(1)
        2. Résolution ALS de u_i avec les données en mémoire O(n_rated × k²)
        3. Mise à jour Top-K index O(n_movies × k)
        Total : < 2ms pour un utilisateur typique MovieLens.
        """
        if user_id not in self.user_map or movie_id not in self.movie_map:
            print("  ⚠️  User ou film inconnu — notation ignorée.")
            return

        user_idx  = self.user_map[user_id]
        movie_idx = self.movie_map[movie_id]

        # [C3] Mise à jour index mémoire O(1)
        if user_idx not in self._ratings_by_user_idx:
            self._ratings_by_user_idx[user_idx] = {}
        self._ratings_by_user_idx[user_idx][movie_idx] = float(rating)

        if user_id not in self.user_rated:
            self.user_rated[user_id] = set()
        self.user_rated[user_id].add(movie_id)

        # [C3] Recalcul vectorisé de u_i depuis l'index mémoire
        rated_dict  = self._ratings_by_user_idx[user_idx]
        rated_idxs  = np.array(list(rated_dict.keys()),  dtype=int)
        rated_vals  = np.array(list(rated_dict.values()), dtype=np.float64)

        V_i      = self.als_model.V[rated_idxs]
        lambda_I = self.als_model.reg_lambda * np.eye(self.als_model.k)
        A        = V_i.T @ V_i + lambda_I
        b        = V_i.T @ rated_vals
        self.als_model.U[user_idx] = np.linalg.solve(A, b)

        # [C3] Mise à jour instantanée du Top-K
        self.index.update_user(user_idx)

        t = self._title.get(movie_id, str(movie_id))
        print(f"  ✅ Noté : '{t}' → {rating}★  "
              f"| Top-K mis à jour ({len(rated_idxs)} films notés)")

    def recommend_cold_start(self, user_id=None, genres=None, K=10):
        if user_id and self.demo._uid_to_pos.get(user_id) is not None:
            return self.cold_start.recommend_by_demo(user_id, K=K)
        if genres:
            return self.cold_start.recommend_by_genre(genres, K=K)
        return self.cold_start.recommend_by_popularity(K=K)


