def load_movielens(data_path="ml-1m/"):
    ratings = pd.read_csv(
        data_path + "ratings.dat", sep="::", engine="python",
        names=["user_id", "movie_id", "rating", "timestamp"]
    )
    movies = pd.read_csv(
        data_path + "movies.dat", sep="::", engine="python",
        names=["movie_id", "title", "genres"], encoding="latin-1"
    )
    users = pd.read_csv(
        data_path + "users.dat", sep="::", engine="python",
        names=["user_id", "gender", "age", "occupation", "zip"]
    )

    # [C6] Suppression des doublons (même user, même film → on garde le dernier)
    before = len(ratings)
    ratings = (ratings
               .sort_values("timestamp")
               .drop_duplicates(subset=["user_id", "movie_id"], keep="last")
               .reset_index(drop=True))
    after = len(ratings)
    if before != after:
        print(f"  ⚠️  {before - after} doublons supprimés dans ratings.")

    return ratings, movies, users


