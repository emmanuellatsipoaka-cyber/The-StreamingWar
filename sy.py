class TopKIndex:
    """
    [C3] Mise à jour en O(n_movies) sans itération sur le DataFrame.
    """

    def __init__(self, U, V, K=20):
        self.K = K
        self.U = U
        self.V = V
        self.top_k = {}
        self._build(U, V)

    def _build(self, U, V, batch_size=500):
        print(f"\n  Pré-calcul Top-{self.K} index...")
        t0 = time.time()
        n  = U.shape[0]
        for s in range(0, n, batch_size):
            e      = min(s + batch_size, n)
            scores = U[s:e] @ V.T               # (batch, n_movies)
            for i, sc in enumerate(scores):
                self._store(s + i, sc)
        print(f"  ✅ Index prêt en {time.time()-t0:.1f}s")

    def _store(self, user_idx, scores):
        top = np.argpartition(scores, -self.K)[-self.K:]
        self.top_k[user_idx] = top[np.argsort(scores[top])[::-1]].copy()

    def update_user(self, user_idx):
        """[C3] Recalcul instantané O(n_movies × k) ≈ 0.1ms."""
        scores = self.U[user_idx] @ self.V.T
        self._store(user_idx, scores)

    def get_top_k(self, user_idx, already_rated=None, K=None):
        K  = K or self.K
        cs = self.top_k.get(user_idx, np.array([], dtype=int))
        if already_rated:
            cs = [x for x in cs if x not in already_rated]
        return list(cs)[:K]


