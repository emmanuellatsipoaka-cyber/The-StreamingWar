def build_sparse_matrix(ratings_df, user_map_input=None, movie_map_input=None):
    if user_map_input is None:
        unique_users = sorted(ratings_df["user_id"].unique())
        user_map_output = {uid: i for i, uid in enumerate(unique_users)}
    else:
        user_map_output = user_map_input

    if movie_map_input is None:
        unique_movies = sorted(ratings_df["movie_id"].unique())
        movie_map_output = {mid: i for i, mid in enumerate(unique_movies)}
    else:
        movie_map_output = movie_map_input

    # Filter out ratings for users/movies not in the provided maps, if maps were provided
    # Also, ensure that only movies/users present in the maps are processed
    filtered_ratings = ratings_df[
        ratings_df["user_id"].map(user_map_output).notna() &
        ratings_df["movie_id"].map(movie_map_output).notna()
    ].copy()

    # Map the IDs to their corresponding indices
    row_indices = filtered_ratings["user_id"].map(user_map_output).values
    col_indices = filtered_ratings["movie_id"].map(movie_map_output).values
    data = filtered_ratings["rating"].values.astype(np.float64)

    # The shape of the sparse matrix should be determined by the size of the maps,
    # not necessarily by the number of unique items in 'filtered_ratings'.
    # This is crucial for consistent matrix dimensions.
    n_users_dim = len(user_map_output)
    n_movies_dim = len(movie_map_output)

    R = csr_matrix((data, (row_indices, col_indices)), shape=(n_users_dim, n_movies_dim))
    print(f"  Matrice R : {R.shape}  —  {R.nnz:,} valeurs non-nulles")
    return R, user_map_output, movie_map_output


